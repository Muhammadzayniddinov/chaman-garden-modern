const langButtons = document.querySelectorAll("[data-lang]");
const navMenu = document.getElementById("navMenu");
const menuToggle = document.querySelector(".menu-toggle");
const siteHeader = document.querySelector(".site-header");
const cursorGlow = document.querySelector(".cursor-glow");
const year = document.getElementById("year");
let currentLanguage = localStorage.getItem("chaman-modern-lang") || "ru";

function getText(key) {
  return translations[currentLanguage]?.[key] || translations.ru[key] || key;
}

function applyLanguage(lang) {
  currentLanguage = translations[lang] ? lang : "ru";
  localStorage.setItem("chaman-modern-lang", currentLanguage);
  document.documentElement.lang = currentLanguage;

  document.querySelectorAll("[data-i18n]").forEach((element) => {
    const key = element.dataset.i18n;
    if (translations[currentLanguage][key]) element.textContent = translations[currentLanguage][key];
  });

  document.querySelectorAll("[data-i18n-placeholder]").forEach((element) => {
    const key = element.dataset.i18nPlaceholder;
    if (translations[currentLanguage][key]) element.placeholder = translations[currentLanguage][key];
  });

  document.querySelectorAll("[data-i18n-content]").forEach((element) => {
    const key = element.dataset.i18nContent;
    if (translations[currentLanguage][key]) element.content = translations[currentLanguage][key];
  });

  document.title = getText("pageTitle");
  langButtons.forEach((button) => button.classList.toggle("active", button.dataset.lang === currentLanguage));
}

function closeMenu() {
  navMenu?.classList.remove("open");
  menuToggle?.classList.remove("active");
  menuToggle?.setAttribute("aria-expanded", "false");
  document.body.classList.remove("menu-open");
}

langButtons.forEach((button) => {
  button.addEventListener("click", () => {
    applyLanguage(button.dataset.lang);
    closeMenu();
  });
});

menuToggle?.addEventListener("click", () => {
  const isOpen = navMenu?.classList.toggle("open");
  menuToggle.classList.toggle("active", Boolean(isOpen));
  menuToggle.setAttribute("aria-expanded", String(Boolean(isOpen)));
  document.body.classList.toggle("menu-open", Boolean(isOpen));
});

document.addEventListener("click", (event) => {
  if (!navMenu?.classList.contains("open")) return;
  const clickedInsideMenu = navMenu.contains(event.target);
  const clickedToggle = menuToggle?.contains(event.target);
  if (!clickedInsideMenu && !clickedToggle) closeMenu();
});

document.addEventListener("keydown", (event) => {
  if (event.key === "Escape") closeMenu();
});

window.addEventListener("resize", () => {
  if (window.innerWidth > 980) closeMenu();
});

function updateHeaderState() {
  siteHeader?.classList.toggle("scrolled", window.scrollY > 18);
}
window.addEventListener("scroll", updateHeaderState, { passive: true });
updateHeaderState();

document.querySelectorAll(".nav a, .nav-menu a").forEach((link) => link.addEventListener("click", closeMenu));

const observer = new IntersectionObserver((entries) => {
  entries.forEach((entry) => {
    if (entry.isIntersecting) {
      entry.target.classList.add("in-view");
      observer.unobserve(entry.target);
    }
  });
}, { threshold: 0.16 });

document.querySelectorAll(".reveal").forEach((element) => observer.observe(element));

document.addEventListener("mousemove", (event) => {
  if (!cursorGlow) return;
  cursorGlow.style.left = `${event.clientX}px`;
  cursorGlow.style.top = `${event.clientY}px`;
});

if (window.matchMedia("(pointer: fine)").matches) {
  document.querySelectorAll(".magnetic").forEach((button) => {
    button.addEventListener("mousemove", (event) => {
      const rect = button.getBoundingClientRect();
      const x = event.clientX - rect.left - rect.width / 2;
      const y = event.clientY - rect.top - rect.height / 2;
      button.style.transform = `translate(${x * 0.08}px, ${y * 0.12}px)`;
    });
    button.addEventListener("mouseleave", () => {
      button.style.transform = "";
    });
  });
}

function removeErrors(form) {
  form.querySelectorAll(".error-text").forEach((element) => element.remove());
  form.querySelectorAll(".error").forEach((element) => element.classList.remove("error"));
}

function showError(input, message) {
  const row = input.closest(".form-row") || input.closest(".consent");
  if (!row) return;
  row.classList.add("error");
  const error = document.createElement("span");
  error.className = "error-text";
  error.textContent = message;
  row.appendChild(error);
}

const requestForm = document.getElementById("requestForm");
requestForm?.addEventListener("submit", (event) => {
  event.preventDefault();
  removeErrors(requestForm);

  const requiredFields = requestForm.querySelectorAll("[required]");
  let valid = true;
  requiredFields.forEach((field) => {
    const isCheckbox = field.type === "checkbox";
    if ((isCheckbox && !field.checked) || (!isCheckbox && !field.value.trim())) {
      showError(field, isCheckbox ? getText("consentRequired") : getText("required"));
      valid = false;
    }
  });

  const status = document.getElementById("formStatus");
  if (!valid) {
    if (status) status.textContent = "";
    return;
  }

  if (status) status.textContent = getText("success");
  const chosenLang = currentLanguage;
  setTimeout(() => {
    window.location.href = `thank-you.html?lang=${encodeURIComponent(chosenLang)}`;
  }, 900);
});

